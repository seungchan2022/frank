//! `M1_diagnosis_data.md` 출력 포맷터.
//!
//! SSOT: 자동 생성 헤더 + 메타 블록 + 측정 표 + 한도 영향 + 실패 로그 + 매핑 표.
//! `report.md` 와 분리: 본 모듈은 data.md만 담당 (사용자 영역은 별도 수동 작성).

use chrono::{DateTime, Utc};

use super::retry_classifier::{ErrorCategory, FailureKind};
use super::variant_generator::VariantKind;

/// 진단 메타 (헤더 블록).
#[derive(Debug, Clone)]
pub struct Meta {
    pub measured_at: DateTime<Utc>,
    pub user_id: String,
    pub tag_count: usize,
    pub variant_count: usize,
    pub limit_requested: usize,
    pub retry_policy: String,
    pub overwrite_policy: String,
    /// 활성 태그 raw — (한국어 이름, created_at).
    pub active_tags: Vec<(String, String)>,
    /// 엔진별 운영 필터 1줄 메모 (예: "Tavily: time_range=week / Exa: startPublishedDate=now-7d").
    pub engine_filter_notes: String,
}

/// 측정 한 셀 — (태그, 변형, 엔진) 3차원 격자의 한 점.
#[derive(Debug, Clone)]
pub struct CellResult {
    pub tag_korean: String,
    pub variant: VariantKind,
    pub engine: String, // "tavily" / "exa"
    pub query: String,
    pub requested: usize,
    pub effective: usize,        // 엔진별 실효 상한 (사전 검증값)
    pub returned: Option<usize>, // 실패 시 None
    /// ≤3개 결과 셀 자동 1회 추가 호출 결과 (없으면 None)
    pub recall_returned: Option<usize>,
    /// 최종 실패 정보 (성공 시 None — 재시도로 회복한 경우에도 None)
    pub failure: Option<FailureInfo>,
    /// 1차 실패 → 2차 재시도 성공 (간헐 회복)으로 기록된 1차 카테고리·메시지.
    /// SSOT DoD #2 "재시도 1회로 영구/간헐 분류" 충족 — 회복 셀도 실패 로그에 narrative 보존.
    pub recovered_after_retry: Option<FailureInfo>,
}

#[derive(Debug, Clone)]
pub struct FailureInfo {
    pub first_category: ErrorCategory,
    pub first_message: String,
    pub second_category: Option<ErrorCategory>,
    /// 보존 목적 — 보고서 작성 시 narrative 인용 가능. 현재 표 출력엔 first_message만 사용.
    #[allow(dead_code)]
    pub second_message: Option<String>,
    pub kind: FailureKind,
}

/// 한도 영향 (사전·진단 호출수·사후).
#[derive(Debug, Clone)]
pub struct QuotaImpact {
    pub engine: String,
    pub remaining_before: u32,
    pub calls_used: u32,
    pub remaining_after_estimated: u32,
}

/// data.md 전체 렌더.
pub fn render(
    meta: &Meta,
    cells: &[CellResult],
    quota: &[QuotaImpact],
    mapping: &[(&str, &str)],
) -> String {
    let mut out = String::new();
    out.push_str(
        "<!-- AUTO-GENERATED by server/src/bin/diagnose_search.rs — DO NOT EDIT (overwrites on re-run) -->\n\n",
    );
    out.push_str("# M1 진단 데이터\n\n");

    // 메타 블록
    out.push_str("## 메타\n\n");
    out.push_str(&format!(
        "- 측정 시각 (UTC): {}\n",
        meta.measured_at
            .to_rfc3339_opts(chrono::SecondsFormat::Secs, true)
    ));
    out.push_str(&format!("- user_id: `{}`\n", meta.user_id));
    out.push_str(&format!(
        "- 활성 태그 수: {} / 변형 수: {} / limit (요청값): {}\n",
        meta.tag_count, meta.variant_count, meta.limit_requested
    ));
    out.push_str(&format!("- 재시도 정책: {}\n", meta.retry_policy));
    out.push_str(&format!("- overwrite 정책: {}\n", meta.overwrite_policy));
    out.push_str(&format!(
        "- 엔진 운영 필터: {}\n\n",
        meta.engine_filter_notes
    ));

    out.push_str("### 활성 태그 raw\n\n");
    out.push_str("| 한국어 | created_at |\n|---|---|\n");
    for (name, created) in &meta.active_tags {
        out.push_str(&format!("| {} | {} |\n", escape_pipe(name), created));
    }
    out.push('\n');

    // 측정 표
    out.push_str("## 측정 결과\n\n");
    out.push_str(
        "| 태그 | 변형 | 엔진 | 쿼리 | requested | effective | returned | recall(2nd) |\n",
    );
    out.push_str("|---|---|---|---|---:|---:|---:|---:|\n");
    for c in cells {
        out.push_str(&format!(
            "| {} | {} | {} | `{}` | {} | {} | {} | {} |\n",
            escape_pipe(&c.tag_korean),
            c.variant.as_label(),
            c.engine,
            escape_pipe(&c.query),
            c.requested,
            c.effective,
            c.returned
                .map(|n| n.to_string())
                .unwrap_or_else(|| "FAIL".to_string()),
            c.recall_returned
                .map(|n| n.to_string())
                .unwrap_or_else(|| "-".to_string()),
        ));
    }
    out.push('\n');

    // 한도 영향
    out.push_str("## 한도 영향\n\n");
    out.push_str("| 엔진 | 사전 잔여 | 진단 호출 | 사후 잔여 (추정) |\n");
    out.push_str("|---|---:|---:|---:|\n");
    for q in quota {
        out.push_str(&format!(
            "| {} | {} | {} | {} |\n",
            q.engine, q.remaining_before, q.calls_used, q.remaining_after_estimated
        ));
    }
    out.push('\n');

    // 실패 로그 (영구 + 회복 양쪽 — SSOT DoD #2 "재시도 1회로 영구/간헐 분류")
    out.push_str("## 실패 로그\n\n");
    let any_log: Vec<(&CellResult, &FailureInfo, bool)> = cells
        .iter()
        .filter_map(|c| {
            if let Some(f) = c.failure.as_ref() {
                Some((c, f, false))
            } else {
                c.recovered_after_retry.as_ref().map(|f| (c, f, true))
            }
        })
        .collect();
    if any_log.is_empty() {
        out.push_str("실패 0건.\n\n");
    } else {
        out.push_str(
            "| 태그 | 변형 | 엔진 | 1차 카테고리 | 2차 카테고리 | 판정 | 회복? | 1차 메시지 |\n",
        );
        out.push_str("|---|---|---|---|---|---|---|---|\n");
        for (c, f, recovered) in any_log {
            out.push_str(&format!(
                "| {} | {} | {} | {} | {} | {:?} | {} | {} |\n",
                escape_pipe(&c.tag_korean),
                c.variant.as_label(),
                c.engine,
                f.first_category,
                f.second_category
                    .map(|c| c.to_string())
                    .unwrap_or_else(|| "-".to_string()),
                f.kind,
                if recovered { "yes" } else { "no" },
                escape_pipe(&redact_secrets(&truncate(&f.first_message, 120))),
            ));
        }
        out.push('\n');
    }

    // 매핑 표
    out.push_str("## 영어 번역 매핑 (재현용)\n\n");
    out.push_str("| 한국어 | 영어 |\n|---|---|\n");
    for (ko, en) in mapping {
        out.push_str(&format!("| {} | {} |\n", escape_pipe(ko), escape_pipe(en)));
    }
    out.push('\n');

    out
}

fn escape_pipe(s: &str) -> String {
    s.replace('|', "\\|")
}

/// API 키·토큰처럼 보이는 패턴을 마스킹. 보수적으로 동작 — 어댑터 에러 메시지가
/// `Authorization` 헤더 조각이나 키 prefix(`tvly-`, `exa-` 등)를 흘릴 때를 대비.
fn redact_secrets(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for token in s.split_whitespace() {
        let cleaned = token.trim_matches(|c: char| !c.is_alphanumeric() && c != '-' && c != '_');
        let looks_like_secret = cleaned.len() >= 24
            && cleaned
                .chars()
                .all(|c| c.is_ascii_alphanumeric() || c == '-' || c == '_');
        if looks_like_secret
            || cleaned.to_lowercase().starts_with("bearer")
            || cleaned.starts_with("tvly-")
            || cleaned.starts_with("exa-")
            || cleaned.starts_with("sk-")
        {
            out.push_str("[REDACTED]");
        } else {
            out.push_str(token);
        }
        out.push(' ');
    }
    out.trim_end().to_string()
}

fn truncate(s: &str, n: usize) -> String {
    if s.chars().count() <= n {
        s.to_string()
    } else {
        let truncated: String = s.chars().take(n).collect();
        format!("{truncated}…")
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    // FailureKind는 super 임포트(상단)에서 가져옴

    fn fixture_meta() -> Meta {
        Meta {
            measured_at: chrono::DateTime::parse_from_rfc3339("2026-05-02T10:00:00Z")
                .unwrap()
                .with_timezone(&Utc),
            user_id: "00000000-0000-0000-0000-000000000001".to_string(),
            tag_count: 1,
            variant_count: 5,
            limit_requested: 100,
            retry_policy: "5xx/timeout/network 1회".to_string(),
            overwrite_policy: "재실행 시 전체 덮어씀".to_string(),
            active_tags: vec![("AI/ML".to_string(), "2026-04-01T00:00:00Z".to_string())],
            engine_filter_notes: "Tavily: time_range=week / Exa: startPublishedDate=now-7d"
                .to_string(),
        }
    }

    // T-04 단위: snapshot — 헤더·메타·표·매핑·실패 섹션 모두 포함
    #[test]
    fn snapshot_contains_all_sections() {
        let cell_ok = CellResult {
            tag_korean: "AI/ML".to_string(),
            variant: VariantKind::Baseline,
            engine: "tavily".to_string(),
            query: "AI/ML latest news".to_string(),
            requested: 100,
            effective: 20,
            returned: Some(15),
            recall_returned: None,
            failure: None,
            recovered_after_retry: None,
        };
        let cell_fail = CellResult {
            tag_korean: "AI/ML".to_string(),
            variant: VariantKind::Simple,
            engine: "tavily".to_string(),
            query: "AI/ML".to_string(),
            requested: 100,
            effective: 20,
            returned: None,
            recall_returned: None,
            failure: Some(FailureInfo {
                first_category: ErrorCategory::ServerErr,
                first_message: "Tavily returned 503: Service Unavailable".to_string(),
                second_category: Some(ErrorCategory::ServerErr),
                second_message: Some("again".to_string()),
                kind: FailureKind::PermanentSameCategory,
            }),
            recovered_after_retry: None,
        };
        let cell_recovered = CellResult {
            tag_korean: "보안".to_string(),
            variant: VariantKind::Baseline,
            engine: "tavily".to_string(),
            query: "보안 latest news".to_string(),
            requested: 100,
            effective: 20,
            returned: Some(7),
            recall_returned: None,
            failure: None,
            recovered_after_retry: Some(FailureInfo {
                first_category: ErrorCategory::Timeout,
                first_message: "request timed out".to_string(),
                second_category: None,
                second_message: None,
                kind: FailureKind::Intermittent,
            }),
        };
        let quota = vec![QuotaImpact {
            engine: "tavily".to_string(),
            remaining_before: 850,
            calls_used: 130,
            remaining_after_estimated: 720,
        }];
        let mapping = vec![("AI/ML", "AI/ML"), ("보안", "Security")];

        let out = render(
            &fixture_meta(),
            &[cell_ok, cell_fail, cell_recovered],
            &quota,
            &mapping,
        );

        assert!(out.contains("AUTO-GENERATED"));
        assert!(out.contains("DO NOT EDIT"));
        assert!(out.contains("# M1 진단 데이터"));
        assert!(out.contains("## 메타"));
        assert!(out.contains("user_id: `00000000-0000-0000-0000-000000000001`"));
        assert!(out.contains("### 활성 태그 raw"));
        assert!(out.contains("## 측정 결과"));
        assert!(out.contains("baseline"));
        assert!(out.contains("FAIL"));
        assert!(out.contains("## 한도 영향"));
        assert!(out.contains("720"));
        assert!(out.contains("## 실패 로그"));
        assert!(out.contains("PermanentSameCategory"));
        // 회복 셀이 실패 로그에 yes로 기록됨 (DoD #2 narrative 보존)
        assert!(out.contains("Intermittent"));
        assert!(out.contains("yes"));
        assert!(out.contains("## 영어 번역 매핑"));
        assert!(out.contains("Security"));
    }

    #[test]
    fn redact_secrets_strips_long_tokens_and_known_prefixes() {
        let s = "Tavily returned 401 with key tvly-abcdef0123456789abcdef Bearer ZZZZZZZZZZZZZZZZZZZZZZZZ";
        let r = redact_secrets(s);
        assert!(!r.contains("tvly-abcdef"));
        assert!(r.contains("[REDACTED]"));
    }

    #[test]
    fn snapshot_no_failures_block_when_empty() {
        let cell = CellResult {
            tag_korean: "AI/ML".to_string(),
            variant: VariantKind::Baseline,
            engine: "exa".to_string(),
            query: "AI/ML latest news".to_string(),
            requested: 100,
            effective: 100,
            returned: Some(40),
            recall_returned: None,
            failure: None,
            recovered_after_retry: None,
        };
        let out = render(&fixture_meta(), &[cell], &[], &[]);
        assert!(out.contains("실패 0건."));
    }

    #[test]
    fn pipe_in_tag_name_is_escaped() {
        let mut meta = fixture_meta();
        meta.active_tags = vec![("AI|ML".to_string(), "2026-04-01T00:00:00Z".to_string())];
        let out = render(&meta, &[], &[], &[]);
        assert!(out.contains("AI\\|ML"));
    }
}
